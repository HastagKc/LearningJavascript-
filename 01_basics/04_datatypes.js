/*

<------------Dynamic Type----------------------------->

JavaScript is a dynamic language with dynamic types. 
Variables in JavaScript are not directly associated 
with any particular value type, and any variable can 
be assigned (and re-assigned) values of all types:

*/

let dynamicTypeExample="Harry";  // string 
console.log(typeof dynamicTypeExample);

dynamicTypeExample=22; // not its a number 
console.log(typeof dynamicTypeExample);

dynamicTypeExample=false; // now it is a boolean data types 
console.log(typeof dynamicTypeExample);

